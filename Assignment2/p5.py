def tuple_operations():
    numbers_tuple = (4, 7, 1, 8, 3)
    
    strings_tuple = ('dog', 'cat', 'elephant', 'apple')

    print("Numerical Tuple:", numbers_tuple)
    print("Length of numerical tuple:", len(numbers_tuple))
    print("Maximum value in numerical tuple:", max(numbers_tuple))
    print("Minimum value in numerical tuple:", min(numbers_tuple))
    print("Sum of numerical tuple:", sum(numbers_tuple))
    print("Sorted numerical tuple:", sorted(numbers_tuple))
    
    print("\nString Tuple:", strings_tuple)
    print("Length of string tuple:", len(strings_tuple))
    print("Maximum value in string tuple:", max(strings_tuple))
    print("Minimum value in string tuple:", min(strings_tuple))
    print("Sorted string tuple:", sorted(strings_tuple))


tuple_operations()
